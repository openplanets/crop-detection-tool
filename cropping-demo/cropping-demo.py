def drop_shadow(img_orig, blur_factor, color=100):

    shadow = np.ones((img_orig.shape[0]+20,img_orig.shape[1]+20,3)) * 255
    shadow[10:shadow.shape[0]-10,10:shadow.shape[1]-10,:] = np.ones((img_orig.shape[0],img_orig.shape[1],3)) * color

    border = 1

    # blur
    for i in range(1,10):
        tmp = cv2.blur(shadow,(blur_factor,blur_factor))
        shadow = np.ones((shadow.shape[0] + 2*border, shadow.shape[1] + 2*border,3)) * 255
        shadow[0:(shadow.shape[0]-2*border),0:(shadow.shape[1]-2*border),:] = tmp

    return shadow.astype(np.uint8)



def paint_image_collection(backgorund, collection):#

    if len(collection) > 0:

        img_idx = 0
        start_x = THUMBNAIL_LIST_LEFT_START
        start_y = THUMBNAIL_LIST_TOP_START

        for img in collection:

            factor = THUMBNAIL_HEIGT / img.shape[0]

            thumbnail = cv2.resize(img, (int(img.shape[1] * factor),int(img.shape[0] * factor)))

            end_y = start_y + thumbnail.shape[0]
            end_x = start_x + thumbnail.shape[1]

            # drop shadow
            shadow = drop_shadow(thumbnail, 6)

            shadow_hsv     = cv2.cvtColor(shadow, cv2.COLOR_BGR2HSV)
            background_hsv = cv2.cvtColor(backgorund, cv2.COLOR_BGR2HSV)
            background_hsv[start_y:(start_y+shadow.shape[0]),start_x:(start_x+shadow.shape[1]),2] *= shadow_hsv[:,:,2] / 255.0
            backgorund     = cv2.cvtColor(background_hsv, cv2.COLOR_HSV2BGR)

            backgorund[start_y:end_y,start_x:end_x] = thumbnail

            start_x = end_x + 2*THUMBNAIL_MARGIN

            img_idx += 1

            if img_idx % THUMBNAILS_PER_ROW == 0:
                start_y = end_y + 2*THUMBNAIL_MARGIN
                start_x = THUMBNAIL_LIST_LEFT_START


    return backgorund


def process_camera_image(frame):

    # rotate image
    #frame = np.rot90(frame, 2)

    if not calibrated:
        calibrateCamera(frame)
    else:
        frame = cv2.warpPerspective(frame,PerspectiveTransform,(frame.shape[1],frame.shape[0]))

    # crop frame
    dst_width = (frame.shape[0] /float(CAMERA_FRAME_HEIGHT)) * float(CAMERA_FRAME_WIDTH)

    #print dst_width

    start_left = int((frame.shape[1] - dst_width) / 2.0)
    end_right = start_left + dst_width

    frame     = frame[:,start_left:end_right,:]

    # resize frame
    disp_frame = cv2.resize(frame, (CAMERA_FRAME_WIDTH,CAMERA_FRAME_HEIGHT))

    return disp_frame

