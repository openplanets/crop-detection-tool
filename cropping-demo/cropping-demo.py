def showCanvasAndWait(curr_canvas, wait_interval=0):

    global output_scale, GLOBAL_WAIT_INTERVAL, WRITE_VIDEO,VIDEO_FPS


    if wait_interval == 0:
        if GLOBAL_WAIT_INTERVAL > 0:
            wait_interval = GLOBAL_WAIT_INTERVAL
        else:
            cv2.putText(curr_canvas, "Press any key to continue...", (600,780), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (100,100,100),2)

    if output_scale != -1:
        curr_canvas = cv2.resize(curr_canvas, (int(curr_canvas.shape[1] * output_scale), int(curr_canvas.shape[0] * output_scale)))

    show_image(curr_canvas)

    # simulate wait time in video
    if WRITE_VIDEO:

        if wait_interval == 0:
            num_frames = VIDEO_FPS * 2
        else:
            num_frames = int(np.round((wait_interval / 1000.0) * VIDEO_FPS)) + 1


        for i in range(num_frames):
            show_image(curr_canvas)

    key = cv2.waitKey(wait_interval)

    if key == 27:
        exit()


