def calculate_perspective_transform(CALIBRATION_COORDS_TOP_LEFT, CALIBRATION_COORDS_TOP_RIGHT, CALIBRATION_COORDS_BOTTOM_LEFT, CALIBRATION_COORDS_BOTTOM_RIGHT):
    bottom_length = CALIBRATION_COORDS_BOTTOM_RIGHT[0] - CALIBRATION_COORDS_BOTTOM_LEFT[0]
#print CALIBRATION_COORDS_TOP_LEFT,CALIBRATION_COORDS_TOP_RIGHT,CALIBRATION_COORDS_BOTTOM_LEFT , CALIBRATION_COORDS_BOTTOM_RIGHT
    pts1 = np.float32([CALIBRATION_COORDS_TOP_LEFT,
            CALIBRATION_COORDS_TOP_RIGHT,
            CALIBRATION_COORDS_BOTTOM_LEFT,
            CALIBRATION_COORDS_BOTTOM_RIGHT])
    pts2 = np.float32([(CALIBRATION_COORDS_BOTTOM_LEFT[0], CALIBRATION_COORDS_BOTTOM_LEFT[1] - bottom_length),
            (CALIBRATION_COORDS_BOTTOM_LEFT[0] + bottom_length, CALIBRATION_COORDS_BOTTOM_LEFT[1] - bottom_length),
            CALIBRATION_COORDS_BOTTOM_LEFT,
            (CALIBRATION_COORDS_BOTTOM_LEFT[0] + bottom_length, CALIBRATION_COORDS_BOTTOM_LEFT[1])])
#pts2 = np.float32([CALIBRATION_COORDS_TOP_LEFT,CALIBRATION_COORDS_TOP_RIGHT,CALIBRATION_COORDS_BOTTOM_LEFT,CALIBRATION_COORDS_BOTTOM_RIGHT])
    PerspectiveTransform = cv2.getPerspectiveTransform(pts1, pts2)
    return PerspectiveTransform

